public class Calculator
{
   public int sum( final int a, final int b )
   {
      return a + b;
   }


   public int minus( final int a, final int b )
   {
      return a - b;
   }


   public int divide( final int a, final int b )
   {
      return a / b;
   }


   public int multiply( final int a, final int b )
   {
      return a * b;
   }
}
